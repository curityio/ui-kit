/*
 * Generates the API Reference docs tree (`content/api-reference/**`) from the single layout declaration in
 * `api-reference.entries.mjs`. Each leaf entry → one MDX page whose body is the export's TSDoc, extracted
 * here and written as real Markdown (+ `<DocExample>` playgrounds); each group → a subfolder with its own
 * `_category_.json`. `sidebar_position` follows declaration order.
 *
 * Emitting real Markdown (rather than mounting a runtime component) lets Docusaurus build the right-hand
 * table of contents and heading anchors natively — same as every other docs page.
 *
 * The output dir is generated (gitignored) — edit `api-reference.entries.mjs` or the SDK's TSDoc, not
 * these files. Run via `npm run docs:apiref` (wired into the pre-docs hooks).
 */

import fs from 'node:fs';
import path from 'node:path';
import { apiReferenceStructure, isApiReferenceGroup } from './api-reference.entries.mjs';
import { extractDocBlocks, blocksToMdx, hasExamples } from './extract-tsdoc.mjs';
import { CONTENT_DIR, SDK_ROOT } from './paths.mjs';

const OUT_DIR = path.join(CONTENT_DIR, 'api-reference');
const GENERATED_NOTE =
  '{/* AUTO-GENERATED by scripts/emit-api-reference.mjs from api-reference.entries.mjs + the SDK TSDoc — do not edit. */}';
const DOC_EXAMPLE_IMPORT = "import DocExample from '@site/src/components/DocExample';";

/**
 * Map each documented SDK export → its API Reference route (`routeBasePath` is `/`, so pages live at
 * `/api-reference/<…>/<id>`). Used to turn `{@link Symbol}` TSDoc tags into internal docs links.
 */
function buildSymbolUrls(items, parentPath, map) {
  for (const item of items) {
    if (isApiReferenceGroup(item)) {
      const groupPath = `${parentPath}/${item.group}`;
      // A group that is itself a documented export renders its `index.mdx` at the group route.
      if (item.id && item.export) {
        map.set(item.export, groupPath);
      }
      buildSymbolUrls(item.items, groupPath, map);
    } else {
      map.set(item.export, `${parentPath}/${item.id}`);
    }
  }
  return map;
}

const SYMBOL_URLS = buildSymbolUrls(apiReferenceStructure, '/api-reference', new Map());
const resolveLink = symbol => SYMBOL_URLS.get(symbol) ?? null;

/** Write a `_category_.json` for a (sub)category folder. `position` is omitted when undefined. */
function writeCategory(dir, label, position) {
  fs.writeFileSync(path.join(dir, '_category_.json'), `${JSON.stringify({ label, position }, null, 2)}\n`);
}

/** The export's TSDoc as an MDX body (real Markdown + `<DocExample>`), with the import line if needed. */
function renderBody(entry) {
  const blocks = extractDocBlocks(path.resolve(SDK_ROOT, entry.file), entry.export, resolveLink);
  const parts = [GENERATED_NOTE];
  if (hasExamples(blocks)) {
    parts.push(DOC_EXAMPLE_IMPORT);
  }
  parts.push(blocksToMdx(blocks));
  return parts.join('\n\n');
}

/** Write one entry's MDX page: frontmatter (title + position) + its TSDoc body. */
function writeEntry(dir, entry, position) {
  const frontmatter = ['---', `title: ${JSON.stringify(entry.label)}`, `sidebar_position: ${position}`, '---'].join(
    '\n'
  );
  fs.writeFileSync(path.join(dir, `${entry.id}.mdx`), `${[frontmatter, renderBody(entry)].join('\n\n')}\n`);
}

/**
 * Write a group's `index.mdx` — the category's own landing page (Docusaurus links the category label to
 * it), with the group export's TSDoc. Used when a group is itself a documented component whose parts are
 * nested under it (e.g. HaapiStepperFormUI with its form fields).
 */
function writeIndex(dir, group) {
  const frontmatter = ['---', `title: ${JSON.stringify(group.label)}`, '---'].join('\n');
  fs.writeFileSync(path.join(dir, 'index.mdx'), `${[frontmatter, renderBody(group)].join('\n\n')}\n`);
}

/** Emit a list of items into `dir`, numbering pages/subcategories by declaration order. */
function emitItems(items, dir) {
  let count = 0;
  items.forEach((item, index) => {
    if (isApiReferenceGroup(item)) {
      const groupDir = path.join(dir, item.group);
      fs.mkdirSync(groupDir, { recursive: true });
      writeCategory(groupDir, item.label, index + 1);
      if (item.id) {
        writeIndex(groupDir, item);
        count += 1;
      }
      count += emitItems(item.items, groupDir);
    } else {
      writeEntry(dir, item, index + 1);
      count += 1;
    }
  });
  return count;
}

fs.rmSync(OUT_DIR, { recursive: true, force: true });
fs.mkdirSync(OUT_DIR, { recursive: true });
writeCategory(OUT_DIR, 'API Reference', 2);
const pages = emitItems(apiReferenceStructure, OUT_DIR);

// eslint-disable-next-line no-console
console.log(`[emit-api-reference] wrote ${pages} pages to content/api-reference/`);
