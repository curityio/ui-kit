/*
 * Generates the "Examples" docs section (`content/examples/**`) from the single layout declaration in
 * `examples.entries.mjs`. Each leaf → one short MDX shell that mounts `<DocExample id=…/>` (a live
 * Sandpack playground for `examples/<id>.tsx`); each group → a subfolder with its own
 * `_category_.json`. `sidebar_position` follows declaration order.
 *
 * The output dir is generated (gitignored) — edit `examples.entries.mjs`, not these files. Run via
 * `npm run docs:examples` (wired into the pre-docs hooks alongside `docs:gen`/`docs:split`/`docs:apiref`).
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { examplesStructure, isExamplesGroup } from '../examples.entries.mjs';

const DOCS_DIR = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '../content');
const OUT_DIR = path.join(DOCS_DIR, 'examples');
const GENERATED_NOTE = '{/* AUTO-GENERATED by scripts/emit-examples.mjs from examples.entries.mjs — do not edit. */}';
const DOC_EXAMPLE_IMPORT = "import DocExample from '@site/src/components/DocExample';";

/** Write a `_category_.json` for a (sub)category folder. `position` is omitted when undefined. */
function writeCategory(dir, label, position) {
  fs.writeFileSync(path.join(dir, '_category_.json'), `${JSON.stringify({ label, position }, null, 2)}\n`);
}

/** Write one entry's MDX shell: frontmatter (title + position) + the `<DocExample>` mount. */
function writeEntry(dir, entry, position) {
  // Quote the title (JSON string = valid YAML double-quoted scalar) so labels with YAML-special chars
  // like `:` (e.g. "Step: data customization") don't break the front matter.
  const frontmatter = ['---', `title: ${JSON.stringify(entry.label)}`, `sidebar_position: ${position}`, '---'].join(
    '\n'
  );
  // Entries may declare extra npm deps (e.g. the captcha example) — forward them as a JSX object prop.
  const depsProp = entry.dependencies ? ` dependencies={${JSON.stringify(entry.dependencies)}}` : '';
  const content = [
    frontmatter,
    GENERATED_NOTE,
    DOC_EXAMPLE_IMPORT,
    `<DocExample id="${entry.id}" label="${entry.label}"${depsProp} />`,
  ].join('\n\n');
  fs.writeFileSync(path.join(dir, `${entry.id}.mdx`), `${content}\n`);
}

/** Emit a list of items into `dir`, numbering pages/subcategories by declaration order. */
function emitItems(items, dir) {
  let count = 0;
  items.forEach((item, index) => {
    if (isExamplesGroup(item)) {
      const groupDir = path.join(dir, item.group);
      fs.mkdirSync(groupDir, { recursive: true });
      writeCategory(groupDir, item.label, index + 1);
      count += emitItems(item.items, groupDir);
    } else {
      writeEntry(dir, item, index + 1);
      count += 1;
    }
  });
  return count;
}

fs.rmSync(OUT_DIR, { recursive: true, force: true });
fs.mkdirSync(OUT_DIR, { recursive: true });
writeCategory(OUT_DIR, 'Examples', 3);
const pages = emitItems(examplesStructure, OUT_DIR);

// eslint-disable-next-line no-console
console.log(`[emit-examples] wrote ${pages} pages to content/examples/`);
